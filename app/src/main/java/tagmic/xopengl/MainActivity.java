/**
 * 通常二维图形库可以绘制点，线，多边形，圆弧，路径等等。
 * OpenGL ES 支持绘制的基本几何图形分为三类：点，线段，三角形。
 * 也就是说OpenGL ES 只能绘制这三种基本几何图形。
 * 任何复杂的2D或是3D图形都是通过这三种几何图形构造而成的
 */
